#include "../push_swap.h"

long	ft_atoi(char *s)
{
	char	n;
	long	res;
	int		i;

	i = 0;
	n = '\0';
	if (s[i] == '+' || s[i] == '-')
		n = s[i++];
	res = 0;
	while (s[i] >= '0' && s[i] <= '9')
		res = res * 10 + (s[i++] - '0');
	if (n == '-')
		res *= -1;
	if (s[i] != '\0')
		return (LONG_MAX);
	return (res);
}
